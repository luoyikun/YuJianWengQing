function TipsCtrl:OpenItem(data, from_view, param_t, close_call_back, show_the_random, gift_id, is_check_item, is_tian_sheng)
	if data == nil then
		return
	end

	local item_cfg, big_type = ItemData.Instance:GetItemConfig(data.item_id)
	if item_cfg == nil then
		return
	end

	-- 形象自选礼包物品
	if big_type == GameEnum.ITEM_BIGTYPE_GIF and item_cfg.gift_type == 4 and from_view ~= TipsFormDef.FROM_BAG and 
		from_view ~= TipsFormDef.FROM_BAG_ON_BAG_STORGE and from_view ~= TipsFormDef.FROM_STORGE_ON_BAG_STORGE then
		MojieData.Instance:SetModelGiftBagIndex(nil)
		MojieData.Instance:SetModelGiftId(data.item_id)
		MojieData.Instance:SetModelGiftData(data)
		MojieData.Instance:SetFromView(from_view)
		ViewManager.Instance:Open(ViewName.ModelGift)
		return
	end

	local is_in_suit, suit_index, index = ClothespressData.Instance:GetItemIsInClothesSuitIndex(data.item_id)
	if data.item_id and is_in_suit then 				--衣柜套装的特殊物品
		if is_in_suit and suit_index >= 0 and index >= 0 then
			ClothespressData.Instance:SetSelectSuitItemIndex(index + 1)
			ClothespressData.Instance:SetSelectSuitIndex(suit_index + 1)
			ClothespressCtrl.Instance:SetCloseCallBack(data, from_view, param_t, close_call_back)
			ViewManager.Instance:Open(ViewName.SuitModelTipView)
			return
		end
	end

	-- 大小目标物品
	if data.item_id and RuneData.Instance:IsRolrGoalItem(data.item_id) then
		local cfg = RuneData.Instance:IsRolrGoalItem(data.item_id)
		local fun = function(click_type)
			RuneCtrl.Instance:SendBigSmallGoalOper(ROLE_BIG_SMALL_GOAL_OPERA_TYPE.ROLE_BIG_SMALL_GOAL_OPERA_FETCH, cfg.system_type, click_type)
		end

		local gold_data = RuneData.Instance:GetGoalData(cfg)
		if not gold_data or not next(gold_data) then return end

		gold_data.from_panel = "bag_view"
		gold_data.call_back = fun
		local is_model
		local is_other_item
		local sys_type = cfg.system_type
		if sys_type == 1 or sys_type == 2 or sys_type == 5 or
			sys_type == 10 or sys_type == 11 then
			is_model = true
			is_other_item = false
		else
			is_model = false
			is_other_item = true
		end
		TipsCtrl.Instance:ShowGoalTimeLimitTitleView(gold_data, is_model, cfg.system_type, is_other_item, close_call_back)
		return
	end

	if item_cfg.sub_type == GameEnum.EQUIP_TYPE_JINGLING_SOUL then
		self.prop_tips:SetData(data, from_view, param_t, close_call_back)
		return
	end
	
	if item_cfg.use_type == GameEnum.ITEM_OPEN_TITLE and from_view ~= TipsFormDef.FROM_SHENGYIN_NOT_USE and from_view ~= TipsFormDef.FROM_SHENGYIN_COMPARE
		and from_view ~= TipsFormDef.FROM_SHENGYIN and from_view ~= TipsFormDef.FROM_SHENGYIN_EXCHANGE then
		self.display_prop_modle_view:SetData(data, from_view, param_t, close_call_back)
		return
	end

	if item_cfg.is_display_role and item_cfg.is_display_role > 0 and item_cfg.is_display_role ~= DISPLAY_TYPE.TITLE then
		if item_cfg.sub_type == GameEnum.EQUIP_TYPE_JINGLING then
			self.spirit_equip_view:SetData(data,from_view, param_t,close_call_back)
		else
			self.display_prop_modle_view:SetData(data, from_view, param_t, close_call_back)
		end
		return
	end

	local gamoe_vo = GameVoManager.Instance:GetMainRoleVo()
	if item_cfg.sub_type == GameEnum.EQUIP_TYPE_JINGLING then
		self.other_equip_tips:Close()
		self.prop_tips:Close()
		self.spirit_equip_view:SetData(data, from_view, param_t,close_call_back)
	elseif item_cfg.use_type == GameEnum.SHENZHOU_WEAPON_TYPE and from_view == TipsFormDef.FROM_SHENZHOU_EQUIP then  		--魔器
		self.magic_item_view:SetData(data, from_view, param_t,close_call_back)
	elseif item_cfg.use_type == GameEnum.TIANSHENHUTI_EQUIP_USE_TYPE then 	--周末装备
		local equip_cfg = TianshenhutiData.Instance:GetEquipCfgByItemId(data.item_id)
		if equip_cfg and equip_cfg.equip_id then
			data.suit_id = equip_cfg.equip_id
		end
		
		if from_view == TipsFormDef.TIANSHENHUTI_BAG and nil ~= TianshenhutiData.Instance:GetCurItemInfo(data.item_id) then
			self.other_role_equip_view:SetData(data, from_view, param_t, close_call_back)
		elseif from_view == TipsFormDef.FROM_TIANSHENHUTI_EQUIP then
			self.equip_tips:SetData(data, from_view, param_t, close_call_back)
		else
			self.equip_tips:SetData(data, from_view, param_t, close_call_back)
		end
	elseif big_type == GameEnum.ITEM_BIGTYPE_EQUIPMENT then					--装备
		local equip_index = EquipData.Instance:GetEquipIndexByType(item_cfg.sub_type)
		if equip_index >= 0 then
			if (item_cfg.sub_type and (item_cfg.sub_type >= GameEnum.E_TYPE_ZHUANZHI_WUQI) and (item_cfg.sub_type <= GameEnum.E_TYPE_ZHUANZHI_YUPEI)) then
				if ForgeData.Instance:GetZhuanzhiEquip(equip_index) and ForgeData.Instance:GetZhuanzhiEquip(equip_index).item_id > 0 and 
					from_view ~= TipsFormDef.FROM_BAG_EQUIP and from_view ~= TipsFormDef.FROM_PLAYER_INFO and (param_t == nil or param_t.role_id == nil or
						param_t.role_id ~= gamoe_vo.role_id) and not data.is_from_extreme then
						self.other_role_equip_view:SetData(data, from_view, param_t, close_call_back, gift_id, is_check_item)
				else
					self.equip_tips:SetData(data, from_view, param_t, close_call_back, gift_id)
				end
			elseif ((item_cfg.sub_type >= GameEnum.E_TYPE_BAIZHAN_WUQI) and (item_cfg.sub_type <= GameEnum.E_TYPE_BAIZHAN_YUPEI)) then
				if ForgeData.Instance:GetBaiZhanEquip(equip_index) and ForgeData.Instance:GetBaiZhanEquip(equip_index).item_id > 0 and 
					from_view ~= TipsFormDef.FROM_BAG_EQUIP and from_view ~= TipsFormDef.FROM_PLAYER_INFO and from_view ~= TipsFormDef.BAIZHAN_SUIT and (param_t == nil or param_t.role_id == nil or
						param_t.role_id ~= gamoe_vo.role_id) and not data.is_from_extreme then
						self.other_role_equip_view:SetData(data, from_view, param_t, close_call_back, gift_id, is_check_item)
				else
					self.equip_tips:SetData(data, from_view, param_t, close_call_back, gift_id)
				end
			elseif DouQiData.Instance:IsDouqiEqupi(data.item_id) then
				-- 斗气装备
				self.equip_tips:SetData(data, from_view, param_t, close_call_back, show_the_random, is_tian_sheng)
			else
				if EquipData.Instance:GetGridData(equip_index) and EquipData.Instance:GetGridData(equip_index).item_id and
					from_view ~= TipsFormDef.FROM_BAG_EQUIP and from_view ~= TipsFormDef.FROM_PLAYER_INFO and (param_t == nil or param_t.role_id == nil or
						param_t.role_id ~= gamoe_vo.role_id) then
						self.other_role_equip_view:SetData(data, from_view, param_t, close_call_back, gift_id, is_check_item)
				else
					self.equip_tips:SetData(data, from_view, param_t, close_call_back, gift_id)
				end
			end
		elseif EquipData.IsMarryEqType(item_cfg.sub_type) then
			local marry_eq_index = MarryEquipData.GetMarryEquipIndex(item_cfg.sub_type)
			local my_data = MarryEquipData.Instance:GetMarryEquipInfo()[marry_eq_index]
			if my_data and next(my_data) and my_data.item_id > 0 and from_view ~= TipsFormDef.FROM_BAG_EQUIP
			and item_cfg.limit_sex == gamoe_vo.sex then
				self.other_equip_compare_view:SetData(data, from_view, param_t, close_call_back, is_tian_sheng)
			else
				self.other_equip_tips:SetData(data, from_view, param_t, close_call_back, show_the_random, is_tian_sheng)
			end
		elseif EquipData.IsShengXiaoEqType(item_cfg.sub_type) then
			local index = item_cfg.sub_type % GameEnum.EQUIP_TYPE_SHENGXIAO_1
			if ShengXiaoData.Instance:GetCurEquipindex(index) > 0 and from_view == TipsFormDef.FROM_SHENGXIAO_BAG then
				self.other_role_equip_view:SetData(data, from_view, param_t, close_call_back, gift_id, is_check_item)
			else
				self.equip_tips:SetData(data, from_view, param_t, close_call_back, gift_id)
			end
		elseif EquipData.IsBianShenEquipType(item_cfg.sub_type) then
			self.equip_tips:SetData(data, from_view, param_t, close_call_back, gift_id)
		elseif EquipData.IsLongQiEqType(item_cfg.sub_type) then
			self.equip_tips:SetData(data, from_view, param_t, close_call_back, gift_id)
		else
			local zs_equip_index = ZhuanShengData.Instance:GetZhuanShengEquipIndex(item_cfg.sub_type)
			local my_data = ZhuanShengData.Instance:GetDressEquipList()[zs_equip_index]
			if my_data and next(my_data) and my_data.item_id > 0 and from_view ~= TipsFormDef.FROM_ZHUANSHENG_VIEW then
				self.other_equip_compare_view:SetData(data, from_view, param_t, close_call_back, is_tian_sheng)
			else
				self.other_equip_tips:SetData(data, from_view, param_t, close_call_back, show_the_random, is_tian_sheng)
			end
		end
		self.prop_tips:Close()
	elseif MojieData.IsMojie(data.item_id) and from_view == TipsFormDef.FROM_BAG_EQUIP then
		self.mojie_tips:SetData(data, from_view, param_t,close_call_back)
	elseif from_view == TipsFormDef.FROM_SHENGYIN_NOT_USE or from_view == TipsFormDef.FROM_SHENGYIN or 
	from_view == TipsFormDef.FROM_SHENGYIN_EXCHANGE or from_view == TipsFormDef.FROM_SHENGYIN_COMPARE then 	-- 圣印
		self.shengyin_tip_view:SetShengYinData(data, from_view, param_t, close_call_back)
	else
		self.equip_tips:Close()
		self.prop_tips:SetData(data, from_view, param_t,close_call_back)
	end
end

