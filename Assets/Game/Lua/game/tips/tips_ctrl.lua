function TipsCtrl:MainuiOpenCreate()
	if IS_AUDIT_VERSION then
		return
	end
	
	--返回
	self.ret_t = nil
	local verify_callback = function(url, arg, data, size)
		self.ret_t = cjson.decode(data)
		if nil == self.ret_t or 0 ~= self.ret_t.ret then
			MainUICtrl.Instance:FlushTipsIcon(MainUIViewChat.IconList.GongGao, false)
			return
		end

		if "table" == type(self.ret_t.data) and nil == next(self.ret_t.data) then
			MainUICtrl.Instance:FlushTipsIcon(MainUIViewChat.IconList.GongGao, false)
			return
		end

		MainUICtrl.Instance:FlushTipsIcon(MainUIViewChat.IconList.GongGao, true)
		self.tips_data:SetGongGaoData(self.ret_t.data)
		self.tips_gonggao:Flush()
	end
	-- 返回结束

	local agent_id = ChannelAgent.GetChannelID()
	local server_id = GameVoManager.Instance:GetMainRoleVo().server_id
	local vip_level = GameVoManager.Instance:GetMainRoleVo().vip_level
	local content = "1"
	local real_url = string.format("%s/api/c2s/fetch_info.php?spid=%s&server=%s&type=%s&vip_level=%s",
									GlobalUrl, tostring(agent_id), tostring(server_id), content, tostring(vip_level))
	HttpClient:Request(real_url, verify_callback)
end

