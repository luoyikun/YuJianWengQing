--管理新系统信息（公告）
function TipsCtrl:NewSystemManager(str)
	if #self.new_system_list > 10 then
		table.remove(self.new_system_list, 1)
	end

	table.insert(self.new_system_list, str)
	if self.system_time_quest then
		return
	end
	self.system_time_quest = GlobalTimerQuest:AddRunQuest(function()
		local contnet = self.new_system_list[1]
		if contnet then
			--如果界面已经打开的话就设置快速关闭
			if self.tips_new_system_notice_view:IsOpen() then
				self.tips_new_system_notice_view:SetEarlyCloseState(true)
				return
			end
		else
			GlobalTimerQuest:CancelQuest(self.system_time_quest)
			self.system_time_quest = nil
			return
		end
		table.remove(self.new_system_list, 1)
		self.tips_new_system_notice_view:SetNotice(contnet)
		self.tips_new_system_notice_view:Open()
	end, 0.1)
end

